export function format(date: string) {
    if (!_.isUndefined(date) && !_.isNull(date)) {
        return dayjs(date).format("DD.MM.YYYY");
    }
    return "";
}

export function getFkDataForColumn(payload: ObjectT, route: string, column: Column) {
    //console.log("PAY__", payload);
    const fkTableName = column.fkTableName as string;
    const cols = column.showProps as string[];
    if (!fkTableName || !cols) {
        return payload[column.dataIndex];
    }
    if (column.fkTableName && _.has(payload, fkTableName) && !_.isNull(payload[fkTableName])) {
        const cols = column.showProps;
        const resultString = _.map(cols, (dataIndex) => {
            if (
                _.has(payload[fkTableName], dataIndex) &&
                !_.isNull(payload[fkTableName][dataIndex])
            ) {
                return payload[fkTableName][dataIndex];
            }
        })
            .filter((value) => !_.isUndefined(value))
            .join(" ");
        return resultString;
    }
}

