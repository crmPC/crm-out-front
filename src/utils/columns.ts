export function getShowOnEntityProperty(payload: ObjectT, model: string) {
    const shProps = showedProps[model];
    if (shProps) {
        let result = "";
        shProps.forEach((prop) => {
            const current = payload[prop];
            if (!_.isUndefined(current) && !_.isNull(current)) {
                result = `${result + payload[prop]} `;
            }
        });
        return result;
    }
    return model;
}

